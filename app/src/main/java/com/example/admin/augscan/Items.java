package com.example.admin.augscan;

public class Items {
    private String itemmilitar;
    private String iteminfo;
    private String itemdata;
    private String itemano;
    private String itemmes;
    private String itemdestino;
    private String itemtipo;
    private String itemmaterial;
    private String itemquantia;


public Items() {

}

public Items(String itemmilitar,String iteminfo,String itemdata,String itemano,String itemmes,String itemdestino,String itemtipo,String itemmaterial,String itemquantia){

    this.itemmilitar=itemmilitar;
    this.iteminfo=iteminfo;
    this.itemdata=itemdata;
    this.itemano=itemano;
    this.itemmes=itemmes;
    this.itemdestino=itemdestino;
    this.itemtipo=itemtipo;
    this.itemmaterial= itemmaterial;
    this.itemquantia= itemquantia;
}

    public String getitemmilitar() {
        return itemmilitar;
    }

    public void setitemmilitar(String itemmilitar) {
        this.itemmilitar = itemmilitar;
    }

    public String getiteminfo() {
        return iteminfo;
    }

    public void setiteminfo(String iteminfo) {
        this.iteminfo = iteminfo;
    }

    public String getitemdata() {
        return itemdata;
    }

    public void setitemano(String itemano) {
        this.itemano = itemano;
    }

    public String getitemano() {
        return itemano;
    }

    public void setitemmes(String itemmes) {
        this.itemmes = itemmes;
    }

    public String getitemmes() {
        return itemmes;
    }

    public void setitemdestino(String itemdestino) {
        this.itemdestino = itemdestino;
    }

    public String getitemdestino() {
        return itemdestino;
    }

    public void setitemtipo(String itemtipo) {
        this.itemtipo = itemtipo;
    }

    public String getitemtipo() {
        return itemtipo;
    }

    public void setitemdata(String itemdata) {
        this.itemdata = itemdata;
    }

    public String getitemmaterial() {
        return itemmaterial;
    }

    public void setitemmaterial(String itemmaterial) {
        this.itemmaterial = itemmaterial;
    }

    public String getitemquantia() {
        return itemquantia;
    }

    public void setitemquantia(String itemquantia) {
        this.itemquantia = itemquantia;
    }

}